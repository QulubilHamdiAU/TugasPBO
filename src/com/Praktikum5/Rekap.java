package com.Praktikum5;

public class Rekap {
    private int nilai;
    private String matkul;
    private String nama;

    void setNilai(int nilai){
        this.nilai = nilai;
    }

    int getNilai(){
        return nilai;
    }

    void setMatkul(String matkul){
        this.matkul = matkul;
    }

    String getMatkul(){
        return matkul;
    }

    void setNama(String nama){
        this.nama = nama;
    }

    String getNama(){
        return nama;
    }
}
